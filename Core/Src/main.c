void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if(htim==&htim10){
		if(spi.pulses){
			switch(spi.pulse_status){
			case 0:					//Initial high time
				HAL_GPIO_WritePin(TEST_GPIO_Port, TEST_Pin,SET);
				spi.pulse_status++;
				break;
			case 1:					// Low time
				HAL_GPIO_WritePin(TEST_GPIO_Port, TEST_Pin,RESET);
				spi.pulses--;
				spi.pulse_status=0;
				break;
			}
		}
	}
}



